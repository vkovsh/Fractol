#include "libft.h"

int	ft_arehex(char *hex)
{
	if (hex)
	{
		if (*hex++ != '0' || *hex++ != 'x')
			return (0);
		while (*hex)
			if (!ft_ishex(*hex++))
				return (0);
		return (1);
	}
	return (0);
}
